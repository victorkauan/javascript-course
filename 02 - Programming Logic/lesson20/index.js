/*
  Comparison operators
  - Greater than: >
  - Greater than or equal to: >=
  - Less than: <
  - Less than or equal to: <=
  - Equal to (value): ==
  - Equal value and equal type: ===
  - Not equal (value): !=
  - Not equal value or not equal type: !==
*/

const number1 = 10;
const number2 = 5;

const comparison = number1 > number2;

console.log(comparison);

